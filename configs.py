def getElectronConfig(atomic_number):
    configuration = (configs[atomic_number])
    return f'"{configuration}"'


def shortConfig(atomic_number):
    groundState = getElectronConfig(atomic_number)
    nobleGasesElement = ['[Ra]', '[Xe]', '[Kr]', '[Ar]', '[Ne]', '[He]']

    # there was some formatting problem with using getelectronconfig for the nobleGasesConfig

    nobleGasesConfig = ['1s2 2s2 2p6 3s2 3p6 3d10 4s2 4p6 4d10 5s2 5p6 4f14 5d10 6s2 6p6',
                        '1s2 2s2 2p6 3s2 3p6 3d10 4s2 4p6 4d10 5s2 5p6',
                        '1s2 2s2 2p6 3s2 3p6 3d10 4s2 4p6',
                        '1s2 2s2 2p6 3s2 3p6',
                        '1s2 2s2 2p6',
                        '1s2',
                        ]

    for x in nobleGasesConfig:
        if x in groundState:
            index = nobleGasesConfig.index(x)
            rest = groundState.replace(x, nobleGasesElement[index])
            return rest
